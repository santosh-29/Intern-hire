    private void applyFilterButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyFilterButtonActionPerformed
        // TODO add your handling code here:
        String query = "SELECT * FROM INTERNSHIP_DETAILS";
        String filter = " WHERE ";
        String selCategory = categoryComboBox.getItemAt(categoryComboBox.getSelectedIndex());
        String selSalary = stipendComboBox.getItemAt(stipendComboBox.getSelectedIndex());
        String selDuration = durationComboBox.getItemAt(durationComboBox.getSelectedIndex());
        String selStipend = "";
        int count = 0;
        if(!selCategory.equals("-"))
        {
            count++;
            filter = filter + "CATEGORY='" + selCategory+"'";
        }
        if(!selDuration.equals("-"))
        {
            if(count>0)
                filter = filter + " AND ";
            count++;
            filter = filter + "PERIOD='" + selDuration+"'";
        }
        if(!selSalary.equals("-"))
        {
            if(selSalary.equals("1000-2000"))
                selStipend = "('1000','2000')";
            else if(selSalary.equals("2000-5000"))
                selStipend = "('2000','3000','4000','5000')";
            else if(selSalary.equals("5000-10000"))
                selStipend = "('5000','6000','7000','8000','9000','10000')";
            else if(selSalary.equals("10000-15000"))
                selStipend = "('10000','11000','12000','13000','14000','15000')";
            else if(selSalary.equals("15000-30000"))
                selStipend = "('15000','16000','17000','18000','19000','20000','21000','22000','23000','24000','25000','26000','27000','28000','29000','30000')";
            
            if(count>0)
            {
                filter = filter + " AND ";
            }
            count++;
            filter = filter + "STIPEND IN " + selStipend;
        }
        if(count>0)
        {
            query = query + filter;
            panelProduct1.removeAll();
            try{  
                Class.forName("oracle.jdbc.driver.OracleDriver");  

                Connection con=DriverManager.getConnection(  
                "jdbc:oracle:thin:@localhost:1521:xe","santosh","oracle");  

                PreparedStatement pstmt = con.prepareStatement(query);
                ResultSet rs = pstmt.executeQuery();
                java.awt.GridBagConstraints gbc = new GridBagConstraints();
                gbc.insets = new Insets(3,0,3,0);
                int i=0;
                while(rs.next())
                {
                    gbc.gridx = 0;
                    gbc.gridy = i++;
                    panelProduct1.add(new InternInternshipPanel(rs.getString(1),rs.getString(2),rs.getString(6),rs.getString(4),rs.getString(5),rs.getString(8),iUsername,rs.getString(7)),gbc);

                    panelProduct1.repaint();
                    panelProduct1.revalidate();
                    jScrollPane1.revalidate();
                }
                if(i==0)
                {
                    panelProduct1.removeAll();
                    panelProduct1.add(new NoResultsFoundPanel());
                    panelProduct1.repaint();
                    panelProduct1.revalidate();
                    jScrollPane1.revalidate();
                }
            con.close();  
            }
            catch(Exception e){ 
                System.out.println(e);
            }
        }
        
     
    }//GEN-LAST:event_applyFilterButtonActionPerformed

