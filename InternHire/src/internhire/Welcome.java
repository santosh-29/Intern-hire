    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        employerImage.setText("");
        try{
            javax.swing.JFileChooser chooser = new javax.swing.JFileChooser();
            chooser.setCurrentDirectory(new File(System.getProperty("user.home")));
            FileNameExtensionFilter extension = new FileNameExtensionFilter("*Images",".png",".jpg",".jpeg");
            chooser.addChoosableFileFilter(extension);
            
            int filestate = chooser.showSaveDialog(null);
            if(filestate == JFileChooser.APPROVE_OPTION)
            {
                File selectedImage = chooser.getSelectedFile();
                image_path = selectedImage.getAbsolutePath();
                if(image_path.endsWith(".jpeg") || image_path.endsWith(".jpg") || image_path.endsWith(".png"))
                {
                    java.awt.Image getAbsolutePath = null;
                    ImageIcon icon = new ImageIcon(image_path);
                    java.awt.Image image = icon.getImage().getScaledInstance(employerImage.getWidth(), employerImage.getHeight(), java.awt.Image.SCALE_SMOOTH);
                    ImageIcon scaled = new ImageIcon(image);
                    employerImage.setIcon(scaled);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Invalid file format.");
                }
            }
            
            FileInputStream in = new FileInputStream(image_path);
            setImage(in);
            
            JOptionPane.showMessageDialog(this,"Success!");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

