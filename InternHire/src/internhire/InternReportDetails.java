    private void reportInternProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportInternProfileActionPerformed
        // TODO add your handling code here:
        String viewIntern;
        ViewCertifyInternProfile internDetails = new ViewCertifyInternProfile(this.internId);
        internDetails.setVisible(true);
        
        
        //internDetails.setInternSkills(internSkills);
        
        try{  
            Class.forName("oracle.jdbc.driver.OracleDriver");  

            Connection con=DriverManager.getConnection(  
            "jdbc:oracle:thin:@localhost:1521:xe","santosh","oracle");  
            
            PreparedStatement pstmt = con.prepareStatement("SELECT * FROM INTERN_DETAILS WHERE username = ?");
            pstmt.setString(1,internId);
            ResultSet rs = pstmt.executeQuery();
            while(rs.next())
            {
                internDetails.setInternAge(rs.getString(3));
                internDetails.setInternContactNumber(rs.getString(6));
                internDetails.setInternEmailId(rs.getString(7));
                internDetails.setInternName(rs.getString(2));
                internDetails.setInternQualification(rs.getString(4));
                internDetails.setInternRating(rs.getString(5));
                internDetails.setInternUsername(rs.getString(1));
            }
            pstmt = con.prepareStatement("SELECT * FROM INTERN_SKILLS WHERE INTERNID = ?");
            pstmt.setString(1,internId);
            rs = pstmt.executeQuery();
            String skills = "";
            while(rs.next())
            {
                skills = skills + rs.getString(2) + ", ";
            }
            internDetails.setInternSkills(skills);
        con.close();  
        }
        catch(Exception e){ 
            System.out.println(e);}                                               

    }//GEN-LAST:event_reportInternProfileActionPerformed

