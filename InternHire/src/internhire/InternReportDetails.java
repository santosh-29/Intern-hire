    private void deleteReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteReportActionPerformed
        // TODO add your handling code here:
        try{  
            Class.forName("oracle.jdbc.driver.OracleDriver");  

            Connection con=DriverManager.getConnection(  
            "jdbc:oracle:thin:@localhost:1521:xe","santosh","oracle");  
            
            PreparedStatement pstmt = con.prepareStatement("DELETE FROM INTERN_REPORTS WHERE EMPLOYERID=? AND INTERNID=? AND INTERNSHIPID=?");
            pstmt.setString(1,employerId);
            pstmt.setString(2,internId);
            pstmt.setString(3,internshipId);
            int rs = pstmt.executeUpdate();
            
            if(rs>0)
            {
                JOptionPane.showMessageDialog(this,"Successfully deleted the report");
                dispose();
                ah.refreshInternReports();
            }
            else {
                JOptionPane.showMessageDialog(this,"Problem occured while deleting the report");
                dispose();
            }
        con.close();  
        }
        catch(Exception e){ 
            System.out.println(e);}
    }//GEN-LAST:event_deleteReportActionPerformed

